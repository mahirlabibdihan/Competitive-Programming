/************ SOLUTION *************/
void solve()
{
    // Adjacent odd - 1
    // No adjacent - 2
    // # of odd segment will be even
    int n;
    cin >> n;
    string s;
    cin >> s;
    char curr = s[0];
    int count = 1;
    int res = 0;
    for (int i = 1; i < n; i++)
    {
        if (s[i] == curr)
        {
            count++;
        }
        else
        {
            if (count % 2)
            {
                res++;
                count++;
            }
            else
            {
                curr = s[i];
                count = 1;
            }
        }
    }
    cout << res << endl;
}
int main()
{
    FAST_IO;
    int T = 1;
    cin >> T;
    for (int t = 1; t <= T; t++)
    {
        solve();
    }
    return EXIT_SUCCESS;
}
