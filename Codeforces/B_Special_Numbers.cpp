/************ SOLUTION *************/
#define MOD 1000000007;
int64 power(int n, int p)
{
    int64 res = 1;
    while (p--)
    {
        res = (res * n) % MOD;
    }
    return res;
}
void solve()
{
    int n, k;
    cin >> n >> k;
    int64 sum = 0;
    for (int i = 0; (1 << i) <= k; i++) // log(k)
    {
        if (k & (1 << i))
        {
            sum = (sum + power(n, i)) % MOD;
        }
    }
    cout << sum << endl;
}
int main()
{
    FAST_IO;
    int T = 1;
    cin >> T;
    for (int t = 1; t <= T; t++)
    {
        solve();
    }
    return EXIT_SUCCESS;
}
